r"""
02_process_sec_comment_letters_for_fce_iv.py

Purpose
-------
Process SEC-originated comment letters (EDGAR form type UPLOAD) and create
firm-year topic variables used to construct Peer SEC Equity Disclosure
Pressure.

This script is meant to be used AFTER crawling SEC comment letters. It takes:
  1) a metadata CSV for SEC UPLOAD letters, ideally generated by the crawler
     01_crawl_sec_upload_comments.py; and
  2) a directory containing downloaded letter text files.

It outputs:
  - item-level classified comments
  - letter-level classified variables
  - firm-year classified variables suitable for merging back to a firm-year panel

Main idea
---------
The goal is not to classify any SEC comment letter as relevant. The target
topic is SEC disclosure pressure about equity-market access in liquidity,
capital-resources, or financing-constraint settings.

The script classifies comment items into:
  - MD&A / liquidity / capital resources comments
  - equity-market-access comments
  - financing-constraint comments
  - debt-financing comments, used to net out debt-oriented financing pressure

Important notes
---------------
1) This is rule-based text classification. It is transparent and replicable,
   but noisy. Manually validate positive and negative examples before using
   the variable in research.
2) Use peer leave-one-out variables, not the firm's own comments, for the main
   peer pressure variable.
3) Use UPLOAD letters, not CORRESP, for SEC-originated disclosure pressure.

Example
-------
python 02_process_sec_comment_letters_for_fce_iv.py ^
    --letters-csv "output/sec_comment_letter_filing_level.csv" ^
    --text-dir "output/comment_texts" ^
    --outdir "output/topic_classifier" ^
    --firm-id-cols gvkey cik10 ^
    --year-col comment_year

The script tries to be robust to different column names used by the crawler:
  cik/cik10, gvkey, accession/accessionNumber, filing_date_public/filingDate,
  comment_year/filing_year, text_path/filename.
"""

